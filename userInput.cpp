# include <iostream>
using namespace std;

int main(){
    int a , b;

    //user input
    cout<<"Enter 1st number"<<endl;
    cin>>a;

    cout<<"Enter the 2nd number"<<endl;
    cin>>b;

    //operators
    cout<<"ADDITION :"<<a + b<<endl;
    cout<<"SUBSTRACTION :"<<a - b<<endl;
    cout<<"MULTIPLICATION :"<<a * b<<endl;
    cout<<"DIVISION :"<<float(a) / float(b)<<endl;


    return 0;
}